package com.ril.jcs.services.compute.model;

import java.io.IOException;
import java.io.StringReader;

import javax.xml.parsers.DocumentBuilder;
import javax.xml.parsers.DocumentBuilderFactory;
import javax.xml.parsers.ParserConfigurationException;

import org.w3c.dom.Document;
import org.w3c.dom.Element;
import org.w3c.dom.Node;
import org.xml.sax.InputSource;
import org.xml.sax.SAXException;

public class ErrorResponse {
		
	public static String Error(final String xmlDoc)
	{
		DocumentBuilder db;
		String ErrorMsg = null;
		try 
		{
			db = DocumentBuilderFactory.newInstance().newDocumentBuilder();
			InputSource is = new InputSource();
			is.setCharacterStream(new StringReader(xmlDoc));

			Document doc;
			doc = db.parse(is);
			Element Root = doc.getDocumentElement();
			Node Ch = Root.getFirstChild().getFirstChild().getFirstChild();
			Ch= Ch.getNextSibling();
			ErrorMsg = Ch.getTextContent();

		} 
		catch (ParserConfigurationException e1) 
		{
			// TODO Auto-generated catch block
			e1.printStackTrace();
		} 
		catch (SAXException e) 
		{
			// TODO Auto-generated catch block
			e.printStackTrace();
		} 
		catch (IOException e) 
		{
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		return ErrorMsg;
	}
}
